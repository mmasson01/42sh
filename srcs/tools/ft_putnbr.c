#include "42sh_tools.h"

void	ft_putnbr(int nbr)
{
	if (nbr > 9 && nbr < 2147483647)
	{
		ft_putnbr(nbr / 10);
		ft_putnbr(nbr % 10);
	}
	else if (nbr < 0 && nbr > -2147483648)
	{
		ft_putchar('-');
		nbr *= -1;
		ft_putnbr(nbr);
	}
	else
	{
		ft_putchar(48 + nbr);
	}
}
