#include "42sh_tools.h"

int		ft_strcmp(char *s1, char *s2)
{
	if (!s1 && !s2)
		return (0);
	while (*(char *)s1 == *(char *)s2 && *s1 != 0 && *s2 != 0)
	{
		s1 = (char *)s1 + 1;
		s2 = (char *)s2 + 1;
	}
	return ((*(unsigned char *)s1) - (*(unsigned char *)s2));
}
