#include "42sh_tools.h"

void	ft_printtable(char **table)
{
	int		i;

	i = 0;
	if (table)
	{
		while (table[i])
		{
			write(1, table[i], ft_strlen(table[i]));
			write(1, "\n", 1);
			i++;
		}
	}
}
