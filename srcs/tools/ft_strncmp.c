#include "42sh_tools.h"

int		ft_strncmp(char *s1, char *s2, size_t n)
{
	if ((s1 == NULL) && (s2 == NULL))
		return (0);
	while (*(char *)s1 == *(char *)s2 && *s1 != 0 && *s2 != 0 && n > 0)
	{
		s1 = (char *)s1 + 1;
		s2 = (char *)s2 + 1;
		n--;
	}
	return ((*(unsigned char *)s1) - (*(unsigned char *)s2));
}
